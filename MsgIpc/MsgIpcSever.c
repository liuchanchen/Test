int main(int argc, char * argv)
{
	int i = 0;
	int ReadLen = 0;
	key_t key = 20;//20是双方商定好的众所周知的key值
	int msgfd = -1;
	msgbufStruct stMsgBuf;

	memset(&stMsgBuf, 0x0, sizeof(stMsgBuf));
	if ((msgfd = msgget(key, IPC_CREAT| SVMSG_MODE))<0)
	{
		perror("msgget error:");
	}
	printf("msgfd:%d\n", msgfd);
	if ((ReadLen = msgrcv(msgfd, &stMsgBuf, 32, 0, 0))<0)
	{
		perror("msgrcv error :");
	}
	printf("read len :%d\n", ReadLen);
	printf("MsgBuf :%s\n", stMsgBuf.MsgBuf);
	msgctl(msgfd, IPC_RMID, NULL);

	return 0;
}
